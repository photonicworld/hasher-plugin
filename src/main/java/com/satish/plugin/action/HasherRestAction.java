package com.satish.plugin.action;

import com.google.common.collect.Maps;
import org.apache.lucene.util.BytesRef;
import org.elasticsearch.ElasticsearchParseException;
import org.elasticsearch.action.bulk.BulkRequest;
import org.elasticsearch.action.bulk.BulkRequestBuilder;
import org.elasticsearch.action.bulk.BulkResponse;
import org.elasticsearch.action.search.SearchResponse;
import org.elasticsearch.client.Client;
import org.elasticsearch.common.hash.MurmurHash3;
import org.elasticsearch.common.hash.MurmurHash3.Hash128;
import org.elasticsearch.common.inject.Inject;
import org.elasticsearch.common.logging.ESLogger;
import org.elasticsearch.common.logging.ESLoggerFactory;
import org.elasticsearch.common.settings.Settings;
import org.elasticsearch.common.unit.TimeValue;
import org.elasticsearch.common.xcontent.XContentBuilder;
import org.elasticsearch.common.xcontent.XContentFactory;
import org.elasticsearch.common.xcontent.XContentParser;
import org.elasticsearch.common.xcontent.XContentParser.Token;
import org.elasticsearch.rest.BaseRestHandler;
import org.elasticsearch.rest.BytesRestResponse;
import org.elasticsearch.rest.RestChannel;
import org.elasticsearch.rest.RestController;
import org.elasticsearch.rest.RestRequest;
import org.elasticsearch.rest.RestStatus;
import org.elasticsearch.search.SearchHit;
import org.elasticsearch.search.sort.SortOrder;
import org.elasticsearch.search.sort.SortParseElement;

import java.io.IOException;
import java.util.Arrays;
import java.util.Map;

import static org.elasticsearch.rest.RestRequest.Method.POST;
import static org.elasticsearch.rest.RestStatus.BAD_REQUEST;

public class HasherRestAction extends BaseRestHandler
{

    private Client client;
    public static String ACTION_PATH = "/{index}/{type}/_hash";
    private ESLogger log = ESLoggerFactory.getLogger(HasherRestAction.class.getName());

    @Inject
    public HasherRestAction(Settings settings,
                            RestController controller,
                            Client client)
    {
        super(settings, controller, client);
        this.client = client;
        controller.registerHandler(POST, ACTION_PATH, this);
    }

    @Override
    protected void handleRequest(RestRequest request,
                                 RestChannel channel,
                                 Client client)
            throws Exception
    {
        //Parsing content does initial sanity check of the request
        try (XContentParser xContent = XContentFactory.xContent(request.content()).createParser(request.content())) {
            HasherRequest hasherRequest = parseAndExecuteRequest(request, xContent);
            log.info("Hasher request [{}]", hasherRequest);
            executeUpdate(hasherRequest, channel, client);
        }
        catch (ElasticsearchParseException e) {
            logger.warn("Bad request", e);
            badRequest(channel, e.getDetailedMessage());
            return;
        }
    }

    private void executeUpdate(HasherRequest request,
                               RestChannel channel,
                               Client client)
            throws IOException
    {
        int SCROLL_TIMEOUT = 60 * 1000;
        SearchResponse searchResponse = client.prepareSearch(request.getIndex())
                .setTypes(request.getType())
                .setExtraSource(request.getQuery())
                .setScroll(new TimeValue(SCROLL_TIMEOUT))
                .setSize(request.getSize()).addSort(SortParseElement.DOC_FIELD_NAME, SortOrder.ASC)
                .setFetchSource(request.getField(), null).execute().actionGet();

        long totalHits = searchResponse.getHits().getTotalHits();
        long totalUpdated = 0;
        boolean hasFailures = false;
        while (true) {
            SearchHit[] searchHits = searchResponse.getHits().getHits();
            BulkRequest bulkRequest = getBulkUpdateRequest(request, searchHits, client);
            if (bulkRequest.numberOfActions() > 0) {
                BulkResponse bulkResponse = client.bulk(bulkRequest).actionGet();
                log.debug("Updated failed: {} took: {}", bulkResponse.hasFailures(), bulkResponse.getTookInMillis());
                totalUpdated += bulkResponse.getItems().length;
            }
            searchResponse = client.prepareSearchScroll(searchResponse.getScrollId()).setScroll(new TimeValue(SCROLL_TIMEOUT)).execute().actionGet();
            if (searchResponse.getHits().getHits().length == 0) {
                break;
            }
        }

        XContentBuilder response = channel.newBuilder()
                .startObject()
                .field("totalHits", totalHits)
                .field("updatedDocuments", totalUpdated)
                .field("failures", hasFailures)
                .endObject();
        channel.sendResponse(new BytesRestResponse(RestStatus.OK, response));
    }

    private BulkRequest getBulkUpdateRequest(HasherRequest request,
                                             SearchHit[] searchHits,
                                             Client client)
    {
        BulkRequestBuilder bulk = client.prepareBulk();
        Arrays.asList(searchHits).stream().map(searchHit -> {
            String index = request.getIndex();
            String type = request.getType();
            Object fieldValue = searchHit.getSource().get(request.getField());
            Map<String, Object> docMapToUpdate = null;
            if (fieldValue != null) {
                docMapToUpdate = Maps.newHashMap();
                docMapToUpdate.put(request.getHashField(), hash((String) fieldValue));
            }
            //Update will insert null if the field does not have any value, which would mostly result in noop
            return client.prepareUpdate(index, type, searchHit.getId()).setDoc(docMapToUpdate).setRetryOnConflict(3).setDetectNoop(true);
        }).forEach(update -> bulk.add(update));

        log.info("Found {} hits created {} update requests in as one bulk", searchHits.length, bulk.numberOfActions());
        return bulk.request();
    }

    /*
     Query is expected to be of form
     {
        "source":{
            "query":{
                ...
            },
            "field":"string_field"
        },
        "dest":{
            "field":"hash_field"
        },
        "size":10 (-1 default)
     }
     */
    private HasherRequest parseAndExecuteRequest(RestRequest restRequest,
                                                 XContentParser parser)
            throws IOException
    {
        Token token = parser.nextToken();
        HasherRequest.Builder requestBuilder = HasherRequest.builder();

        String index = restRequest.param("index");
        String type = restRequest.param("type");

        //These are assumed to be present as they are registered as path params
        requestBuilder.setIndex(index);
        requestBuilder.setType(type);

        String currentFieldName = null;

        if (token != Token.START_OBJECT) {
            throw new ElasticsearchParseException("Request body must be an object");
        }
        while ((token = parser.nextToken()) != XContentParser.Token.END_OBJECT) {
            if (token == XContentParser.Token.FIELD_NAME) {
                currentFieldName = parser.currentName();
            }
            else if (token == XContentParser.Token.START_OBJECT) {
                switch (currentFieldName) {
                    case "source":
                        parseSource(parser, requestBuilder);
                        break;
                    case "dest":
                        parseDest(parser, requestBuilder);
                        break;
                    default:
                        throw new ElasticsearchParseException("Unknown object field [{}]", currentFieldName);
                }
            }
            else if (token == XContentParser.Token.START_ARRAY) {
                throw new ElasticsearchParseException("Unknown array field [{}]", currentFieldName);
            }
            else if (token.isValue()) {
                switch (currentFieldName) {
                    case "size":
                        requestBuilder.setSize(parser.intValue());
                        break;
                    default:
                        throw new ElasticsearchParseException("Unknown value field [{}]", currentFieldName);
                }
            }
            else {
                throw new ElasticsearchParseException("Unexpected token type [{}]", token);
            }
        }
        return requestBuilder.build();
    }

    private void badRequest(RestChannel channel,
                            String message)
    {
        try {
            XContentBuilder builder = channel.newErrorBuilder();
            channel.sendResponse(new BytesRestResponse(BAD_REQUEST, builder.startObject().field("error", message).endObject()));
        }
        catch (IOException e) {
            logger.warn("Failed to send response", e);
        }
    }

    private void parseSource(XContentParser parser,
                             HasherRequest.Builder requestBuilder)
            throws IOException
    {
        Map<String, Object> source = parser.map();
        Object field = source.get("field");
        if (field != null && field instanceof String) {
            requestBuilder.setField((String) field);
            source.remove("field");
        }
        else {
            throw new ElasticsearchParseException("[field] required inside source");
        }

        XContentBuilder builder = XContentFactory.contentBuilder(parser.contentType());
        builder.map(source);
        requestBuilder.setQuery(builder.bytes());
    }

    private void parseDest(XContentParser parser,
                           HasherRequest.Builder requestBuilder)
            throws IOException
    {
        String currentFieldName = null;
        XContentParser.Token token;
        while ((token = parser.nextToken()) != XContentParser.Token.END_OBJECT) {
            if (token == XContentParser.Token.FIELD_NAME) {
                currentFieldName = parser.currentName();
            }
            else if (token == XContentParser.Token.START_OBJECT) {
                throw new ElasticsearchParseException("Unknown object field [{}]", currentFieldName);
            }
            else if (token == XContentParser.Token.START_ARRAY) {
                throw new ElasticsearchParseException("Unknown array field [{}]", currentFieldName);
            }
            else if (token.isValue()) {
                switch (currentFieldName) {
                    case "field":
                        requestBuilder.setHashField(parser.text());
                        break;
                    default:
                        throw new ElasticsearchParseException("Unknown value field [{}]", currentFieldName);
                }
            }
            else {
                throw new ElasticsearchParseException("Unexpected token type [{}]", token);
            }
        }
    }

    public static long hash(String string)
    {
        BytesRef bytes = new BytesRef(string);
        //This line is as is in elasticsearch
        /*See <a
         href="https://github.com/elastic/elasticsearch/blob/master/plugins/mapper-murmur3/src/main/java/org/elasticsearch/index/mapper/murmur3/Murmur3FieldMapper.java#L157">
         Murmur3 mapper field
         </a>
        */
        //Seed should be 0 and uses first hash from the murmur3 hash
        return MurmurHash3.hash128(bytes.bytes, bytes.offset, bytes.length, 0, new Hash128()).h1;
    }

}
